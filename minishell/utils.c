#include "struct.h"

char *trim_quotes(char *str)
{
    int i;
	int j;

	j = 0;
	i = 0;

    while (str[i])
	{
        if (str[i] != '\'' && str[i] != '\"')
		{
            str[j] = str[i];
            j++;
        }
        i++;
    }
	return (str);
}
